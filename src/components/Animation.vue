      tl.to(centers, { scale: 0.3, opacity: 0, duration: 0.2 });
      tl.to(centers, { scale: 0.3, opacity: 1, duration: 0.2 });

      // centers
      tl.addLabel('centers', '>');
      tl.fromTo(
