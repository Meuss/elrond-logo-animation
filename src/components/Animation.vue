import { DrawSVGPlugin } from 'gsap/DrawSVGPlugin';
import { MotionPathPlugin } from 'gsap/MotionPathPlugin';
import { GSDevTools } from 'gsap/GSDevTools';
gsap.registerPlugin(DrawSVGPlugin, MotionPathPlugin, GSDevTools);

export default {
  setup() {
    const tl = gsap.timeline();

    function hide(el) {
      tl.set(el, { opacity: 0 }, 0);
    }
    function moveCenter(el, x, y) {
      const moveAmount = 39;
      tl.to(el, { x: x * moveAmount, y: y * moveAmount, duration: 0.8, ease: 'expo.out' }, 'centers+=1');
    }
    function lines(el) {
      tl.fromTo(el, { opacity: 0 }, { opacity: 1, duration: 0.3, stagger: 0.1 }, 'lines');
      tl.fromTo(el, { opacity: 1 }, { opacity: 0, duration: 0.3, stagger: 0.1 }, 'lines+=0.1');
    }
    function linesReverse(el) {
      tl.fromTo(el.reverse(), { opacity: 0 }, { opacity: 1, duration: 0.2, stagger: 0.06 }, 'lines-reverse-=0.2');
    }
    onMounted(() => {
      // get elements
      const centers = gsap.utils.toArray('#centers path');
      const topright = gsap.utils.toArray('#topright path').reverse();
      const topleft = gsap.utils.toArray('#topleft path').reverse();
      const bottomright = gsap.utils.toArray('#bottomright path');
      const bottomleft = gsap.utils.toArray('#bottomleft path');
      const hideElements = [
        '#big path',
        '#external path',
        '#topright path',
        '#bottomright path',
        '#bottomleft path',
        '#topleft path',
        '#center',
        '.dot',
      ];
      // initial setup
      gsap.set('#big path', { drawSVG: '0' });
      gsap.set('#logo', { x: 100, rotate: 135, transformOrigin: '50% 50%' });
      gsap.set('#text', { x: 100 });
      gsap.set('#motionPath', { x: 100, stroke: 'none' });
      hideElements.forEach((el) => hide(el));

      // motionPath
      tl.to(
        '.dot',
        {
          motionPath: {
            path: '#motionPath',
            align: '#motionPath',
          },
          duration: 3,
          ease: 'power4.inOut',
        },
        '+=0',
      );
      tl.to('.dot', { opacity: 1, duration: 1 }, '-=2');
      tl.to('.dot', { opacity: 0, duration: 1 }, '-=1.2');

      // centers
      tl.addLabel('centers', '>');
      tl.fromTo(
        centers,
        { scale: 0, opacity: 1, transformOrigin: '50% 50%' },
        { scale: 1, duration: 0.5, ease: 'back.out', id: 'centers' },
      );
      moveCenter(centers[0], -1, -1);
      moveCenter(centers[1], 1, -1);
      moveCenter(centers[2], 1, 1);
      moveCenter(centers[3], -1, 1);

      tl.addLabel('lines', '>');
      lines(topright);
      lines(topleft);
      lines(bottomright);
      lines(bottomleft);

      tl.addLabel('rotate', '>');
      tl.to('#logo', { rotate: 0, duration: 1, ease: 'back.inOut(1.3)', id: 'rotate' }, 'rotate-=0.2');

      tl.addLabel('lines-reverse', '>+=0.1');
      tl.fromTo('#center', { opacity: 0 }, { opacity: 1, duration: 0.2, id: 'lines-reverse' }, 'lines-reverse');
      linesReverse(topright);
      linesReverse(topleft);
      linesReverse(bottomright);
      linesReverse(bottomleft);

      tl.addLabel('draw', '>');
      tl.set('#big path', { opacity: 1, fill: 'none', id: 'draw' }, 'draw+=0.5');
      tl.to('#big path', { drawSVG: '100%', duration: 1 });
      tl.to('#big path', { fill: 'white', duration: 0.5 });

      tl.addLabel('slide', '>');
      tl.to('#logo', { x: 0, rotate: -90, duration: 1, ease: 'expo.inOut', id: 'slide' }, 'slide');
      tl.to('#text', { x: 0, duration: 1, ease: 'expo.inOut' }, 'slide');
      tl.fromTo(
        '#text',
        { opacity: 0, scale: 0.9, transformOrigin: '0% 50%' },
        { opacity: 1, scale: 1, duration: 0.6, ease: 'expo.out' },
        'slide+=0.5',
      );

      GSDevTools.create();
    });
  },
};
</script>

